def run_menu(students):
    while True:

        print("""Choose an option:
1. Add Student
2. View Students
3. Find Oldest Student
4. Update Student
5. Remove Student
6. Search Student
7. Youngest Student
8. Average age
9. Counts students over a threshold
10. Exit Program
11. Counts students below a threshold
12. Student Summary Report
13. View sorted student report""")

        choice = input("Enter your choice: ")

        if not choice.isdigit():
            print("Invalid Input")
            continue

        choice = int(choice)

        if choice == 10:
            print("Program Exited.")
            break

        if choice not in menu:
            print("Invalid choice.")
            continue

        # Get menu details
        entry = menu[choice]
        function = entry["function"]
        func_type = entry["type"]

        if func_type == "print":
            function(students)

            if entry["changes_data"]:
                save_students(students)

        elif func_type == "return":
            result = function(students)

            if result is None:
                print("No students available.")
            else:
                print(result)

        elif func_type == "input":
            inputs = entry["inputs"]
            input_values = {}

            for input_name, input_type in inputs.items():
                # Example:
                # input_name = "threshold"
                # input_type = int

                user_value = input(f"Enter {input_name}: ")

                try:
                    converted_value = input_type(user_value)

                except ValueError:
                    print(f"Invalid {input_name}.")
                    break

                input_values[input_name] = converted_value

            else:
                result = function(students, *input_values.values())
                print(result)

