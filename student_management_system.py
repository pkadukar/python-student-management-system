def add_student(students):
  while True: #outer loop
    Name = input("Enter name of student or ! to stop: ").strip()

    if Name == "!":
      print("Adding students stopped.")
      break

    if not valid_name(Name):  #if the name is not valid continue the loop --> take new input. if not True then continue if not False do not continue
      continue

    while True: #inner loop
      Age = input("Enter age of student: ")

      if not valid_age(Age):
        continue

      Age = int(Age)
      break

    student = {"Name": Name, "Age": Age}
    students.append(student)
    save_students(students)
    print("Student added successfully.")

