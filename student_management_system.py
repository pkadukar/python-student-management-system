def average_age(students):

  if not students:
        return None

  total_age = 0

  for item in students:
    total_age += item["Age"]

  average = total_age/len(students)
  return average

def count_students_above_age(students, threshold):

  if not students:
    return 0

  count = 0

  for item in students:
    if item["Age"] > threshold:
      count += 1

  return count

def count_students_below_age(students,threshold):

  if is_empty(students):
    #print("List is empty.")
    return 0

  count = 0

  for item in students:
    if item["Age"] < threshold:
      count += 1
  return count

